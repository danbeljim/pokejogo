// Live cache — starts as copy of fallback, overwritten by fetch
export const baseStatsCache: Record<number, [number, number, number, number]> = { ...FALLBACK }

export function getBaseStats(dexId: number): [number, number, number, number] {
  return baseStatsCache[dexId] ?? [45, 50, 45, 45]
}

export async function loadAllBaseStats(dexIds: number[]): Promise<void> {
  const unique = [...new Set(dexIds)]
  await Promise.all(unique.map(async id => {
    try {
      const res = await fetch(`https://pokeapi.co/api/v2/pokemon/${id}`)
      if (!res.ok) return
      const data = await res.json()
      const s = data.stats as { base_stat: number; stat: { name: string } }[]
      const get = (name: string) => s.find(x => x.stat.name === name)?.base_stat ?? 45
      baseStatsCache[id] = [get('hp'), get('attack'), get('defense'), get('speed')]
    } catch {
      // keep fallback
    }
  }))
}
